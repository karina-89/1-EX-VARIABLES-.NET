using System;

namespace _1_ex_variables
{
    class Program
    {
        static void Main(string[] args)
        {
            string nombre = "Karina";
            string apellido1 = "Rodriguez";
            string apellido2 = "Ramirez";

            int dia = 25;
            int mes = 5;
            int ano = 1984;

            Console.WriteLine(apellido1 + " " + apellido2 + ", " + nombre);
            Console.WriteLine(dia + "/" + mes + "/" + ano);
        }
    }
}
